#!/usr/bin/env python
"""
    This Module contains a function concat That concatinate the value
    of two strings that a passed
"""


def concat(str1: str, str2: str) -> str:
    """
        This Function is used to concatinate two string.

        Arguments:
            str1: The first string
            str2: The Second string
    """
    return "{}{}".format(str1, str2)
