#!/usr/bin/env python3
"""
    This Module contains a function add That accepts two float
    parameters
"""


def add(a: float, b: float) -> float:
    """
        This Function is for addition

        Arguments:
            a: This is the first variable of type float
            b: This is the second variable of type float

        Return: This returns the addition of both variables in float
    """
    return (a + b)
